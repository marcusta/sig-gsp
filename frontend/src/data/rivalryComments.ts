/**
 * Get a random comment based on the rivalry balance
 * @param balance The rivalry balance (positive = winning, negative = losing)
 * @param rivalName The rival player's name to insert into the comment
 * @returns A randomized comment appropriate for the balance
 */
export function getRivalryComment(balance: number, rivalName: string): string {
  let category: keyof typeof rivalryComments;

  if (balance >= 5) {
    category = "stronglyWinning";
  } else if (balance >= 3) {
    category = "mildlyWinning";
  } else if (balance >= 1) {
    category = "slightlyWinning";
  } else if (balance === 0) {
    category = "even";
  } else if (balance >= -2) {
    category = "slightlyLosing";
  } else if (balance >= -4) {
    category = "mildlyLosing";
  } else {
    category = "stronglyLosing";
  }

  const comments = rivalryComments[category];
  const randomComment = comments[Math.floor(Math.random() * comments.length)];

  return randomComment.replace(/\{rival\}/g, rivalName);
}
